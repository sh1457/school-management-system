CREATE TABLE FEES_INFO
(
 STANDARD VARCHAR2(3),
 AGE NUMBER(12),
 STDFEES NUMBER(8,2),
TRANSPORTFEE NUMBER(8,2),
CONSTRAINT FEES_INFO_PK PRIMARY KEY(STANDARD)
 );

CREATE TABLE STUDENT_INFO
(
 STUDENT_ID VARCHAR2(5),
 STUDENT_NAME VARCHAR2(30),
 DATE_OF_BIRTH DATE,
GUARDIAN_TYPE CHAR,
GUARDIAN_NAME VARCHAR2(30),
ADDRESS VARCHAR2(50),
COUNTRY_ID VARCHAR2(5),
CONTACT_NO NUMBER(10),
MAIL_ID VARCHAR2(30),
GENDER CHAR,
STANDARD VARCHAR2(3),
STANDARD_CATEGORY CHAR,
TRANSPORT CHAR,
NET_FEES NUMBER(7,2),
CONSTRAINT STUDENT_INFO_PK PRIMARY KEY(STUDENT_ID),
CONSTRAINT COUNTRY_INFO_FK FOREIGN KEY(COUNTRY_ID) REFERENCES COUNTRY_INFO(COUNTRY_ID),
CONSTRAINT FEES_INFO_FK FOREIGN KEY(STANDARD) REFERENCES FEES_INFO(STANDARD),
CONSTRAINT CHK_GUARDIAN_TYPE CHECK (GUARDIAN_TYPE IN('P','G')),
CONSTRAINT CHK_GENDER CHECK (GENDER IN('M','F')),
CONSTRAINT CHK_STANDARD_CATEGORY CHECK (STANDARD_CATEGORY IN('P','S','H')),
CONSTRAINT CHK_TRANSPORT CHECK (TRANSPORT IN('Y','N'))
 );

CREATE TABLE COUNTRY_INFO
(
 COUNTRY_ID VARCHAR2(5),
 COUNTRY_NAME VARCHAR2(25),
 STATE_NAME VARCHAR2(25),
CONSTRAINT COUNTRY_INFO_PK PRIMARY KEY(COUNTRY_ID)
 );
