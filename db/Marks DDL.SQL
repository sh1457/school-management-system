CREATE TABLE STUDENT_MARK_PRIMARY
(
 STUDENT_ID VARCHAR2(5),
ENGLISH_MARK NUMBER(3),
HINDI_MARK NUMBER(3),
SCIENCE_MARK NUMBER(3),
MATHS_MARK NUMBER(3),
TOTAL_MARK NUMBER(3),
AVERAGE_MARK NUMBER(5,2),
GRADE CHAR,
CONSTRAINT STUDENT_MARK_PRIMARY_PK PRIMARY KEY(STUDENT_ID),
CONSTRAINT CHK_GRADE_PRIMARY CHECK (GRADE IN('A','B','C','F'))
);

