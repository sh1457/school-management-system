CREATE TABLE STAFF_SALARY
(
 YEAR_OF_EXPERIENCE NUMBER(2),
 HIGHEST_QUALIFICATION VARCHAR2(10),
 SALARY NUMBER(7,2),
CONSTRAINT STAFF_SALARY_PK PRIMARY KEY(SALARY)
 );

CREATE TABLE STAFF_INFO
(
 STAFF_ID VARCHAR2(5),
 STAFF_NAME VARCHAR2(25),
 DATE_OF_BIRTH DATE,
ADDRESS VARCHAR2(50),
COUNTRY_ID VARCHAR2(5),
GENDER CHAR,
SALARY NUMBER(7,2),
HIGHEST_QUALIFICATION VARCHAR2(20),
EMAIL_ID VARCHAR2(20),
CONTACT_NUMBER NUMBER(10),
DESIGNATION VARCHAR2(20),
YEAR_PASSED_OUT NUMBER(5),
EXPERIENCE NUMBER(2),
DOJ DATE,
CONSTRAINT STAFF_INFO_PK PRIMARY KEY(STAFF_ID),
CONSTRAINT COUNTRY_INFO_FK2 FOREIGN KEY(COUNTRY_ID) REFERENCES COUNTRY_INFO(COUNTRY_ID),
CONSTRAINT STAFF_SALARY_FK FOREIGN KEY(SALARY) REFERENCES STAFF_SALARY(SALARY),
CONSTRAINT CHK_GENDER2 CHECK (GENDER IN('M','F'))
);

CREATE TABLE COUNTRY_INFO
(
 COUNTRY_ID VARCHAR2(5),
 COUNTRY_NAME VARCHAR2(25),
 STATE_NAME VARCHAR2(25),
CONSTRAINT STAFF_SALARY_PK PRIMARY KEY(COUNTRY_ID)
 );
